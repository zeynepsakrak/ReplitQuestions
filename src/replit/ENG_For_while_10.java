package replit;

public class ENG_For_while_10 {
    public static void main(String[] args) {
        /*

Write a Program to find the sum of natural numbers from 1 to 100.

OUTPUT : Sum of Natural Numbers 5050

*/
        int top=0;
        for (int i = 0; i <= 100; i++) {
            top+=i;
        }
        System.out.println(top);
    }
}
